bool marioCollidingbottom(Player p, Block b) {

	return (p.bottom.y >= b.top.y) &&
		(p.bottom.x >= b.left.x) &&
		(p.bottom.x <= b.right.x);
}

bool marioCollidingAbove(Player p, Block b) {

	return (p.top.y <= b.bottom.y) &&  // Mario est√° por debajo del bloque
		(p.top.y > b.top.y) &&  // Mario no est√° por encima del bloque
		(p.top.x >= b.left.x) &&  // Mario est√° dentro del rango horizontal izquierdo
		(p.top.x <= b.right.x);  // Mario est√° dentro del rango horizontal derecho
}

